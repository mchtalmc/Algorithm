package BigO;

public class On {
    //Elimizdeki veri seti arttikca , algoritmanin calisma suresiyle dogru orantili
    //olarak artis gosteren algoritmalar O(n) algoritmalardir.

    public static void main(String[] args) {


        int [] arr = {123,321,321,3,213,21,32,13,21};

        for (int a : arr ) { //Eleman sayisi kadar donecektir. Algoritamnin calisma suresi eleman sayisiyla dogru orantili olarak artacaktir.
            System.out.println(a);
        }




    }
}
